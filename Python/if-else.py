# If condition
age = 25
if age >= 18:
    print("Hmm, You are adult.")


# If else condition
age = 16
if age >= 18:
    print("Hmm, You are adult.")
else:
    print("You are not adult.")


# Nested If else condition
age = 18
if age < 2:
    print("You are baby.")
elif age < 11:
    print("You are children.")
elif age < 18:
    print("You are adolescents.")
else:
    print("You are adult.")